# 1. Given two variables 'a' and 'b', write an if-elif-else statement that checks:
#    - If 'a' is greater than 'b', print "a is greater than b".
#    - If 'a' is less than 'b', print "b is greater than a".
#    - If 'a' is equal to 'b', print "a and b are equal".

a=4;
b=6;

if (a>b):
    print("a is greater than b");
elif(a<b):
    print("a is less than b");
else:
    print("a is equal to b");